def texas(bird_population):
    '''
    (int) -- > int
    Ask for the bird population in Texas and then make a sentence out of it. 
    '''
    print ("The great state of Texas has", bird_population, "birds.")

def california(bird_population):
    '''
    (int) -- > int
    Ask for the bird population in California and then make a sentence out of it. 
    '''
    print ("California has", bird_population, "as long as it does not fall into the ocean.")

